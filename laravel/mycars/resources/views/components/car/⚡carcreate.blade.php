<?php

use Livewire\Component;
use Livewire\WithFileUploads;
use Livewire\Attributes\On;
use Livewire\Attributes\Validate;
use App\Models\Car;
use Illuminate\Support\Facades\Auth;

new class extends Component {
    use WithFileUploads;
    public $mostrar = false;
    #[Validate('required')]
    public $matricula;
    #[Validate('required')]
    public $marca;
    #[Validate('required')]
    public $modelo;
    #[Validate('required')]
    public $color;
    #[Validate('required')]
    public $year;
    #[Validate('required')]
    public $precio;
    #[Validate('required')]
    public $fecha_ultima_revision;
    #[Validate('required')]
    public $foto;
    public function mostrarform()
    {
        $this->mostrar = !$this->mostrar;
    }

    public function guardar()
    {
        $nombreFoto = time() . '_' . $this->foto->getClientOriginalName();
        $this->foto->storeAs('imgcars', $nombreFoto);
        Car::create([
            'matricula' => $this->matricula,
            'marca' => $this->marca,
            'modelo' => $this->modelo,
            'color' => $this->color,
            'year' => $this->year,
            'precio' => $this->precio,
            'fecha_ultima_revision' => $this->fecha_ultima_revision,
            'foto' => $nombreFoto,
            'user_id' => Auth::id(),
        ]);
        $this->dispatch('insertCar');
        $this->reset();
    }
};
